# Nhập vào từ bàn phím hai số thực number1, number2. Hãy in ra tổng, hiệu, tích, thương, chia nguyên, chia lấy dư, lũy thừa của hai số đó
number1 = input('nhap number 1: ')
number2 = input('nhap number 2: ')
# tong
nb1 = int(number1)
nb2 = int(number2)
print('tong =', nb1 + nb2)
# hieu
print('hieu =', nb1 - nb2)
# Tich
print('tich =', nb1 * nb2)
# thuong
print('thuong =', nb1 / nb2)
# chia nguyen
print('chia nguyen =', nb1 // nb2)
# chia lay du
print('chia du = ', nb1 % nb2)
# luy thua
print('luy thua =', nb1 ** nb2)
